package com.game.www.wfcc.function.openLottery.bean.tuijian;

public class RecommendInfo {
    private int id;

    private String versusText;

    private String matchDateText;

    private String matchName;

    private String analysisText;

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return this.id;
    }

    public void setVersusText(String versusText) {
        this.versusText = versusText;
    }

    public String getVersusText() {
        return this.versusText;
    }

    public void setMatchDateText(String matchDateText) {
        this.matchDateText = matchDateText;
    }

    public String getMatchDateText() {
        return this.matchDateText;
    }

    public void setMatchName(String matchName) {
        this.matchName = matchName;
    }

    public String getMatchName() {
        return this.matchName;
    }

    public void setAnalysisText(String analysisText) {
        this.analysisText = analysisText;
    }

    public String getAnalysisText() {
        return this.analysisText;
    }

}